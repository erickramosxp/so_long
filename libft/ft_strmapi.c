#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		size;
	char	*s1;

	if (!s || !f)
		return (0);
	size = ft_strlen(s);
	i = 0;
	s1 = (char *)malloc((size + 1) * sizeof(char));
	if (!s1)
		return (0);
	while (s[i] != '\0')
	{
		s1[i] = f(i, s[i]);
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
